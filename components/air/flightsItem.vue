        </el-col>
      </el-row>
    </div>
    <div class="flight-recommend">
      <!-- 隐藏的座位信息列表 -->
      <el-row
        v-show="isShow"
        type="flex"
        justify="space-between"
        align="middle"
        v-for="(e,i) in data.seat_infos"
        :key="i"
      >
        <el-col :span="4">低价推荐</el-col>
        <el-col :span="20">
          <el-row type="flex" justify="space-between" align="middle" class="flight-sell">
