            </el-col>
          </el-row>
        </el-col>
      </el-row>
    </div>
  </div>
</template>

<script>
export default {
  props: {
    // 数据
    data: {
      type: Object,
      // 默认是空数组
      default: {}
    }
  },
  data() {
    return {
      // 控制低价机票座位信息列表的隐藏和显示
      isShow: false
    };
  },
  watch:{
    data(){
      this.isShow=false
    }
  },
  computed: {
    rankTime() {
      const arr = this.data.arr_time;
      const dep = this.data.dep_time;
      let start = dep.split(":");
      let end = arr.split(":");
      // 如果开始的时间是小于结束的时间，加24小时
      if (start[0] > end[0]) {
        end[0] = +end[0] + 24;
      }
		const dis=((end[0]*60-start[0]*60)+(end[1]-start[1]))
		const hours=Math.floor(dis/60) 
		const secends=dis%60
      return hours+"小时"+secends+"分";
    }
  },
  methods: {
    // 跳转到订单页
    handleToOrder(e){
      // /airs/:id
    }
  }
};
</script>

